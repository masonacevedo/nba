CREATE TABLE IF NOT EXISTS games (
    SEASON_ID INTEGER,
    TEAM_ID INTEGER,
    TEAM_ABBREVIATION TEXT,
    TEAM_NAME TEXT,
    GAME_ID INTEGER,
    GAME_DATE TEXT,
    MATCHUP TEXT,
    WL TEXT,
    MIN REAL,
    FGM INTEGER,
    FGA INTEGER,
    FG_PCT REAL,
    FG3M INTEGER,
    FG3A INTEGER,
    FG3_PCT REAL,
    FTM INTEGER,
    FTA INTEGER,
    FT_PCT REAL,
    OREB INTEGER,
    DREB INTEGER,
    REB INTEGER,
    AST INTEGER,
    STL INTEGER,
    BLK INTEGER,
    TOV INTEGER,
    PF INTEGER,
    PTS INTEGER,
    PLUS_MINUS REAL,
    VIDEO_AVAILABLE BOOLEAN,
    PRIMARY KEY (game_id, team_id)
);
